function createPlot(w, h, pad, maxQ, maxP) {
  return {
    maxQ,
    maxP,
    x: (q) => pad + (q / maxQ) * (w - pad * 1.45),
    y: (p) => h - pad - (p / maxP) * (h - pad * 1.55)
  };
}

function inverseDemand(q, a, b) {
  return (a - q) / b;
}

function inverseSupply(q, c, d, tax = 0) {
  return (q - c) / d + tax;
}

function sampleLine(plot, fn, steps = 72) {
  const pts = [];
  for (let i = 0; i <= steps; i += 1) {
    const q = (plot.maxQ * i) / steps;
    const p = fn(q);
    if (Number.isFinite(p) && p >= 0 && p <= plot.maxP * 1.05) pts.push([plot.x(q), plot.y(p)]);
  }
  return pts;
}

function fillDataPolygon(ctx, plot, points, fill) {
  const valid = points.filter(([q, p]) => Number.isFinite(q) && Number.isFinite(p));
  if (valid.length < 3) return;
  ctx.save();
  ctx.fillStyle = fill;
  ctx.beginPath();
  valid.forEach(([q, p], index) => {
    const px = plot.x(q);
    const py = plot.y(p);
    if (index === 0) ctx.moveTo(px, py);
    else ctx.lineTo(px, py);
  });
  ctx.closePath();
  ctx.fill();
  ctx.restore();
}

function drawArrow(ctx, x1, y1, x2, y2, color, width = 3) {
  const angle = Math.atan2(y2 - y1, x2 - x1);
  const head = 10;
  ctx.save();
  ctx.strokeStyle = color;
  ctx.fillStyle = color;
  ctx.lineWidth = width;
  ctx.lineCap = "round";
  ctx.beginPath();
  ctx.moveTo(x1, y1);
  ctx.lineTo(x2, y2);
  ctx.stroke();
  ctx.beginPath();
  ctx.moveTo(x2, y2);
  ctx.lineTo(x2 - head * Math.cos(angle - Math.PI / 6), y2 - head * Math.sin(angle - Math.PI / 6));
  ctx.lineTo(x2 - head * Math.cos(angle + Math.PI / 6), y2 - head * Math.sin(angle + Math.PI / 6));
  ctx.closePath();
  ctx.fill();
  ctx.restore();
}

