function setupPolishMotion() {
  const progress = document.getElementById("readingProgress");
  const updateProgress = () => {
    if (!progress) return;
    const maxScroll = document.documentElement.scrollHeight - window.innerHeight;
    const ratio = maxScroll > 0 ? window.scrollY / maxScroll : 0;
    progress.style.width = `${Math.max(0, Math.min(1, ratio)) * 100}%`;
  };
  updateProgress();
  window.addEventListener("scroll", updateProgress, { passive: true });

  const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (prefersReducedMotion || !("IntersectionObserver" in window)) {
    revealMotionReady = true;
    applyRevealMotion(document, true);
    return;
  }
  revealObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.18, rootMargin: "0px 0px -90px 0px" });
  revealMotionReady = true;
  applyRevealMotion(document);
}

function applyRevealMotion(root = document, instant = false) {
  const selector = ".hero-copy > *, .hero-visual, .chapter-card, .learning-section, .formula-strip, .callout, .note-block, .visual-card, .diagram-card, .panel, .metric, .exam-pack .blueprint-card, .case-shell, .case-question, .case-answer, .scenario-card, .practice-area, .quiz-card, .blank-item, .short-item, .flashcard, .checklist label";
  const scope = root instanceof Element ? root : document;
  const matches = [];
  if (scope.matches?.(selector)) matches.push(scope);
  matches.push(...scope.querySelectorAll(selector));
  matches
    .filter((target) => !target.classList.contains("reveal"))
    .forEach((target, index) => {
      target.classList.add("reveal");
      target.style.setProperty("--reveal-delay", `${Math.min((index % 8) * 70, 490)}ms`);
      if (instant || !revealObserver) {
        target.classList.add("is-visible");
      } else {
        revealObserver.observe(target);
      }
    });
}

