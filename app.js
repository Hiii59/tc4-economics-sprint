function startHeroCanvas() {
  const canvas = document.getElementById("heroMarketCanvas");
  const ctx = canvas.getContext("2d");
  let t = 0;
  function frame() {
    t += 0.012;
    const w = canvas.width;
    const h = canvas.height;
    ctx.clearRect(0, 0, w, h);
    ctx.fillStyle = colors.graph;
    ctx.fillRect(0, 0, w, h);
    ctx.strokeStyle = colors.grid;
    ctx.lineWidth = 1;
    for (let x = 36; x < w; x += 44) {
      ctx.beginPath();
      ctx.moveTo(x, 0);
      ctx.lineTo(x, h);
      ctx.stroke();
    }
    for (let y = 32; y < h; y += 44) {
      ctx.beginPath();
      ctx.moveTo(0, y);
      ctx.lineTo(w, y);
      ctx.stroke();
    }

    const pad = 62;
    ctx.strokeStyle = colors.axis;
    ctx.lineWidth = 2;
    ctx.beginPath();
    ctx.moveTo(pad, 40);
    ctx.lineTo(pad, h - pad);
    ctx.lineTo(w - 46, h - pad);
    ctx.stroke();

    const wave = Math.sin(t) * 18;
    const demand = [[pad + 34, 72 + wave], [w - 88, h - 104 - wave * 0.25]];
    const supply = [[pad + 38, h - 92 + wave * 0.28], [w - 94, 88 - wave]];
    drawPolyline(ctx, demand, colors.coral, 5);
    drawPolyline(ctx, supply, colors.teal, 5);
    const ex = (demand[0][0] + demand[1][0]) / 2 + Math.sin(t * 1.5) * 14;
    const ey = (demand[0][1] + demand[1][1]) / 2;
    ctx.fillStyle = "rgba(246,189,65,.22)";
    ctx.beginPath();
    ctx.moveTo(pad + 34, 72 + wave);
    ctx.lineTo(ex, ey);
    ctx.lineTo(pad + 34, ey);
    ctx.closePath();
    ctx.fill();
    ctx.fillStyle = "rgba(19,169,154,.20)";
    ctx.beginPath();
    ctx.moveTo(pad + 38, h - 92 + wave * 0.28);
    ctx.lineTo(ex, ey);
    ctx.lineTo(pad + 38, ey);
    ctx.closePath();
    ctx.fill();
    ctx.shadowColor = "rgba(255,214,110,.8)";
    ctx.shadowBlur = 24;
    dot(ctx, ex, ey, colors.amber);
    ctx.shadowBlur = 0;
    ctx.fillStyle = colors.ink;
    ctx.font = `800 22px ${canvasFontStack}`;
    ctx.fillText("P*", ex + 18, ey - 14);
    ctx.fillText("Q*", ex - 8, h - pad + 34);
    ctx.fillStyle = colors.coral;
    ctx.fillText("Demand", w - 190, h - 130);
    ctx.fillStyle = colors.teal;
    ctx.fillText("Supply", w - 180, 104);
    ctx.fillStyle = colors.muted;
    ctx.fillText("CS + PS = Social Welfare", 86, 52);
    requestAnimationFrame(frame);
  }
  frame();
}

function fmt(value) {
  if (!Number.isFinite(Number(value))) return "-";
  const rounded = Math.round(Number(value) * 100) / 100;
  return Number.isInteger(rounded) ? String(rounded) : rounded.toFixed(2);
}

function randInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function randPick(items) {
  return items[randInt(0, items.length - 1)];
}

function normalize(value) {
  return String(value).trim().toLowerCase().replace(/\s+/g, " ").replaceAll(" ", "");
}
