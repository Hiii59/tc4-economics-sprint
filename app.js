  drawPolyline(ctx, supplyLine, colors.teal, 4);
  if (tax > 0) drawPolyline(ctx, taxLine, colors.violet, 3);

  drawDashed(ctx, x(0), y(result.pStar), x(result.qStar), y(result.pStar), colors.muted);
  drawDashed(ctx, x(result.qStar), y(0), x(result.qStar), y(result.pStar), colors.muted);

