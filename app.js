function checkDrill() {
  const inputs = [...document.querySelectorAll("[data-answer-index]")];
  const mistakes = [];
  inputs.forEach((input, index) => {
    const expected = currentDrill.fields[index].answer;
    const raw = input.value.trim();
    let ok = false;
    if (typeof expected === "number") {
      ok = Math.abs(Number(raw) - expected) <= Math.max(0.05, Math.abs(expected) * 0.005);
    } else if (Array.isArray(expected)) {
      ok = expected.map(normalize).includes(normalize(raw));
    }
    input.style.borderColor = ok ? "#86efac" : "#fca5a5";
    if (!ok) mistakes.push(currentDrill.fields[index].label);
  });
  const feedback = document.getElementById("drillFeedback");
  if (mistakes.length === 0) {
    feedback.className = "feedback good";
    feedback.innerHTML = `ถูกครบครับ ${inline(currentDrill.solution)}`;
  } else {
    feedback.className = "feedback warn";
    feedback.innerHTML = `ยังพลาด: ${inline(mistakes.join(", "))}<br>${inline(currentDrill.solution)}`;
  }
}

